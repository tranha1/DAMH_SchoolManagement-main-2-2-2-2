    public void showData(){
        DefaultTableModel tableModel1;
        jTable1.getModel();
        tableModel1=(DefaultTableModel)jTable1.getModel();
        tableModel1.setRowCount(0);
        String query = "select MaCB, Thang, Nam, LuongCoSo, LuongCung, PhuCapThamNien, PhuCapChucVu, TongLuong from bangluongchitiet where Thang ='"+ThongKeLuongTheoThangFrame.Thang+"' and Nam='"+ThongKeLuongTheoThangFrame.Nam+"'";
        System.out.println(query);
        try{
            Connection conn = getConnection();
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(query);
            while(rs.next()){
                tableModel1.addRow(new Object[]{
                    rs.getInt("MaCB"), rs.getInt("Thang"), rs.getInt("Nam"), rs.getInt("LuongCoSo"), rs.getInt("LuongCung"), rs.getInt("PhuCapThamNien"), rs.getInt("PhuCapChucVu"), rs.getInt("TongLuong")
                });
            }
        }catch(Exception e){}
    }

